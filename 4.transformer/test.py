# 统一定义一个时间计算函数
def calculate_time_str(time_list):
    total_seconds = 0
    for t in time_list:
        m, s = map(int, t.split(':'))
        total_seconds += m * 60 + s
    hours = total_seconds // 3600
    minutes = (total_seconds % 3600) // 60
    seconds = total_seconds % 60
    return f"{hours:02d}:{minutes:02d}:{seconds:02d}"

